/**
 * Get meals by category
 */
export function getMealsByCategory(category: MealType): MealDatabaseEntry[] {
  return mealDatabase.filter((meal) => meal.category === category);
}

/**
 * Get all unique tags from the database
 */
export function getAllTags(): MealTag[] {
  const tags = new Set<MealTag>();
  mealDatabase.forEach((meal) => {
    meal.tags.forEach((tag) => tags.add(tag));
  });
  return Array.from(tags);
}
